import { Routes } from '@angular/router';

export const routes: Routes = [
  {
    path: '',
    redirectTo: 'home',
    pathMatch: 'full',
  },
  {
    path: 'home',
    loadComponent: () => import('./pages/home/home.page').then( m => m.HomePage)
  },
  {
    path: 'home/chat/:id',
    loadComponent: () => import('./pages/home/chat/chat.page').then( m => m.ChatPage)
  },
];
